public class Main {

	public static void main(String[] args) {

        // These are method calls
		sayHello(); 
		saySomething("Yey");

	}

	// This is a method that prints hello world to STDOUT
	public static void sayHello() {

		System.out.println("Hello, World!");

	}

	// This is a method that prints a message to STDOUT
	public static void saySomething(String msg) {

		System.out.println(msg);

	}

}

